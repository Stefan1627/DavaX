-- Dimension tables for data warehouse
CREATE TABLE dim_department (
    department_key     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    department_id_src  NUMBER,
    department_name    VARCHAR2(100),
    cost_center        VARCHAR2(20)
);

CREATE TABLE dim_employee (
    employee_key         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id_src      NUMBER,
    employee_code        VARCHAR2(20),
    first_name           VARCHAR2(50),
    last_name            VARCHAR2(50),
    email                VARCHAR2(100),
    hire_date            DATE,
    employment_status    VARCHAR2(20),
    department_key       NUMBER,
    CONSTRAINT fk_dim_employee_department
        FOREIGN KEY (department_key) REFERENCES dim_department(department_key)
);

CREATE TABLE dim_project (
    project_key        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id_src     NUMBER,
    project_code       VARCHAR2(20),
    project_name       VARCHAR2(100),
    start_date         DATE,
    end_date           DATE,
    project_status     VARCHAR2(20),
    budget_hours       NUMBER(8,2)
);

CREATE TABLE dim_date (
    date_key           NUMBER PRIMARY KEY,
    full_date          DATE NOT NULL,
    day_number         NUMBER,
    month_number       NUMBER,
    month_name         VARCHAR2(20),
    quarter_number     NUMBER,
    year_number        NUMBER,
    weekday_name       VARCHAR2(20),
    is_weekend         CHAR(1)
);

CREATE TABLE dim_activity_type (
    activity_type_key   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_system       VARCHAR2(20),
    activity_category   VARCHAR2(30),
    activity_subtype    VARCHAR2(30),
    billable_flag       CHAR(1)
);



-- Fact table for timesheet entries
CREATE TABLE fact_employee_activity (
    activity_fact_key      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date_key               NUMBER NOT NULL,
    employee_key           NUMBER NOT NULL,
    department_key         NUMBER NOT NULL,
    project_key            NUMBER,
    activity_type_key      NUMBER NOT NULL,
    source_record_id       NUMBER,
    source_system          VARCHAR2(20) NOT NULL,
    activity_description   VARCHAR2(255),
    hours_amount           NUMBER(6,2) DEFAULT 0,
    activity_count         NUMBER DEFAULT 1,

    CONSTRAINT fk_fact_date
        FOREIGN KEY (date_key) REFERENCES dim_date(date_key),
    CONSTRAINT fk_fact_employee
        FOREIGN KEY (employee_key) REFERENCES dim_employee(employee_key),
    CONSTRAINT fk_fact_department
        FOREIGN KEY (department_key) REFERENCES dim_department(department_key),
    CONSTRAINT fk_fact_project
        FOREIGN KEY (project_key) REFERENCES dim_project(project_key),
    CONSTRAINT fk_fact_activity_type
        FOREIGN KEY (activity_type_key) REFERENCES dim_activity_type(activity_type_key)
);